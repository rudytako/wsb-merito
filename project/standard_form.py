def quadratic_to_standard(a: float , b :float, c: float) -> str:
    """
    Converts quadratic function from standard form to standard form.
    
    Args:
        a (float): Coefficient of x^2.
        b (float): Coefficient of x.
        c (float): Constant term.
        
    Returns:
        str: Standard form of the quadratic function.
    """
    return f"{a}x^2 + {b}x + {c}"
